    private int verificarPosicaoLabel() {
        if (jLabelNum1.getText().equals("")) {
            return 0;
        } else if (jLabelNum2.getText().equals("")) {
            return 1;
        } else if (jLabelNum3.getText().equals("")) {
            return 2;
        } else if (jLabelNum4.getText().equals("")) {
            return 3;
        } else {
            return 4;
        }
    }

    private void atribuirValorLabel(String s) {
        int posicao = verificarPosicaoLabel();
        switch (posicao) {
            case 0:
                jLabelNum1.setText(s);
                break;
            case 1:
                jLabelNum2.setText(s);
                break;
            case 2:
                jLabelNum3.setText(s);
                break;
            case 3:
                jLabelNum4.setText(s);
                String sNumero = jLabelNum1.getText() + jLabelNum2.getText() + jLabelNum3.getText() + jLabelNum4.getText();
                if (vereador) {
                    Candidato candidato = candidatoC.pesquisarCandidato(Integer.parseInt(sNumero), TipoCargo.VEREADOR);
                    if (candidato != null) {
                        jLabelStringNome.setText("NOME: ");
                        jLabelStringPartido.setText("PARTIDO: ");
                        jLabelNome.setText(candidato.getNome());
                        jLabelPartido.setText(candidato.getNomePartido());
                    } else {
                        jLabelStringNome.setText("NOME: ");
                        jLabelStringPartido.setText("PARTIDO ");
                        jLabelNome.setText("Nenhum Candidato");
                        jLabelPartido.setText("Nenhum Candidato");

                    }
                } else {
                    Candidato candidato = candidatoC.pesquisarCandidato(Integer.parseInt(sNumero), TipoCargo.PREFEITO);
                    if (candidato != null) {
                        jLabelStringNome.setText("NOME: ");
                        jLabelStringPartido.setText("PARTIDO ");
                        jLabelNome.setText(candidato.getNome());
                        jLabelPartido.setText(candidato.getNomePartido());
                    } else {
                        jLabelStringNome.setText("NOME: ");
                        jLabelStringPartido.setText("PARTIDO ");
                        jLabelNome.setText("Nenhum Candidato");
                        jLabelPartido.setText("Nenhum Candidato");

                    }
                }

                break;
            case 4:
                break;
        }
    }

    private void limparLabel() {
        jLabelStringNome.setText("");
        jLabelStringPartido.setText("");
        jLabelNome.setText("");
        jLabelPartido.setText("");
        jLabelNum1.setText("");
        jLabelNum2.setText("");
        jLabelNum3.setText("");
        jLabelNum4.setText("");

    }

    private void votar() throws IOException {
        limparLabel();
        if (vereador) {
            vereador = false;
            jLabelCargo.setText("PREFEITO");
        } else {
            MenuMesario mM = new MenuMesario(apuracaoC, votacaoC, candidatoC, eleitorC, mesarioC);
            mM.setVisible(true);
            this.setVisible(false);
        }
    }
