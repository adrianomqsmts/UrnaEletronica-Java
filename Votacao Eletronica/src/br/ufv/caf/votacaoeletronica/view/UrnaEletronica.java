    private void buttonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConfirmarActionPerformed
        try {
            String sNumero = jLabelNum1.getText() + jLabelNum2.getText() + jLabelNum3.getText() + jLabelNum4.getText();
            if (sNumero.equals("")) {
                sNumero = "0";
            }
            if (vereador) {
                votacaoC.votarVereador(Integer.parseInt(sNumero));
            } else {
                votacaoC.votarPrefeito(Integer.parseInt(sNumero));
            }

            votar();
        } catch (IOException ex) {
            Logger.getLogger(UrnaEletronica.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonConfirmarActionPerformed



